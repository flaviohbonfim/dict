// Converte shortcodes :emoji: para emojis no texto
export function convertEmojiShortcodes(text: string): string {
  let result = text;
  
  // Regex para encontrar :shortcode: patterns
  const emojiRegex = /:([a-z0-9_+-]+):/g;
  
  result = result.replace(emojiRegex, (match, shortcode) => {
    return emojiShortcodes[shortcode] || match;
  });
  
  return result;
}
